
s = 'whats up'
print(s.capitalize()) # capitalizes first charater of string
print(s.upper())
print(s.find('u')) # gives the location of character
print(s.split(' '))
print(s.center(20, 'X'))
print(s.isalnum()) # check if the string is alpha numeric
print(s.isalpha()) # gives string with no numbers of special characters
# "islower" to check if all characters are in lower case or not
# endswith to check if string ends with that character
print(s[-1] == 'p') # will print true, because string ends with p



